#include <stdio.h>

int main()
{

	int inteiro = 939;
	float decimal = 9.39;
	char caractere = 'x';
	
	printf("int: %i\n", inteiro);
	printf("float:%f\n", decimal);
	printf("char: %c\n", caractere);
	
	return 0;	
}
/*
Dica:
%i -> int(inteiro)
%f -> float(decimal)
%c -> char(caractere)
*/
